public class Hensu{
	public static void main(String[] args){
		//変数(integer:整数)
		int money;
		money = 100;
		System.out.println("所持金" + money);
		System.out.println("50円拾った");
		money = money + 50;
		System.out.println("所持金" + money);
		System.out.println("1000円拾った");
		money = money + 1000;
		System.out.println("所持金" + money);
		System.out.println("500円の牛丼食べたよ");
		money = money - 500;
		System.out.println("所持金" + money);
	}
}
