import java.util.*;
public class PrintMethod{
	public static void main(String[] args){
		printMethod("佐々木");
	}

	public static void printMethod(String name){
		System.out.println(name + "は走った");
		System.out.println("お腹の肉が気になり走った");
		System.out.println("頑張って走った結果、" + name + "はちょっと痩せた");
		System.out.println(name + "は痩せて嬉しくなって、お菓子を食べた");
		System.out.println();
		System.out.println("太った");
	}
}
